def areaCal(length,width):
    '''
    funtion for calculating area of a space. Expects two
    arguments length and width
    '''
    return length * width

def circCal(rad):
    '''
    funtion for calculating the circumference of a 
    circle given its radius. Expects one argument "rad"
    for the radius.

    If diameter(dia) is used try syntax:

    circCal(dia/2)
    '''
    pi = 22/7
    return 2 * rad * pi
